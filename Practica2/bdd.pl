% ------------- Menu inicio -------------
inicio :- write('***** Bienvenido ***'), nl, nl,
	write('Selecciona una opcion'), nl,
	format('\t[1] Planificar viaje
    \t[2] Reportes
	-> '),
	read(OP_INICIO), opcion_inicio(OP_INICIO).
opcion_inicio(OP_INICIO) :- (
    OP_INICIO == 1 -> menu_criterios;
    OP_INICIO == 2 -> menu_reportes
).

% -----------------------------------------
% ------------- Planificacion -------------
% -----------------------------------------

menu_criterios :- write('Selecciona el criterio mas importante'), nl,
	format('\t[1] Presupuesto
    \t[2] Idioma
    \t[3] Cantidad de estrellas del hotel
    \t[4] Clima
	-> '),
	read(OP_CRITERIO), opcion(OP_CRITERIO).
	
opcion(OP_CRITERIO) :- (
	OP_CRITERIO == 1 -> presupuesto;
	OP_CRITERIO == 2 -> idioma;
	OP_CRITERIO == 3 -> estrellas;
	OP_CRITERIO == 4 -> clima
).

% ------------- Presupuesto -------------
presupuesto :- write('Cual es tu presupuesto?'),
	read(PRESUPUESTO), tipos_presupuesto(PRESUPUESTO).
	
tipos_presupuesto(PRESUPUESTO) :- (
	PRESUPUESTO < 1000 -> bajo_presupuesto(PRESUPUESTO);
	PRESUPUESTO >= 3000 -> alto_presupuesto(PRESUPUESTO);
	medio_presupuesto(PRESUPUESTO)
).

bajo_presupuesto(PRESUPUESTO) :- write('** Plan de viaje economico **'), nl,
	write('\tCual es tu presupuesto maximo a gastar en comida?: '),
    read(COMIDA_MAX),
    write('\tCual es tu presupuesto maximo a gastar en la habitacion?: '),
    read(HABITACION_MAX),
    write('\tCual es la puntuacion minima de los clientes? (0-10): '),
    read(OPINION_MIN),
    write('\tCuantas personas viajaran?: '),
    read(CANTIDAD_PERSONAS),
    write('\tCual es el tipo de habitacion? (simple, doble): '),
    read(TIPO_HABITACION),
    write('\tCuantos dias se hospedaran?: '),
    read(CANTIDAD_DIAS),
    write('\tCuantos tiempos de comida incluiras diarios? (0-3): '),
    read(TIEMPOS_COMIDA),
    write('\tViajas en vehiculo? (s/n): '),
    read(VEHICULO),
    inf_bajo_presupuesto(PRESUPUESTO, COMIDA_MAX, HABITACION_MAX, OPINION_MIN, CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO).

medio_presupuesto(PRESUPUESTO) :- write('** Plan de viaje simple **'), nl,
    write('\tQue clima prefieres en el hotel? (calor, frio, templado, tropical): '),
    read(CLIMA),
    write('\tCual es la distancia maxima del viaje? (km): '),
    read(DISTANCIA_MAX),
    write('\tCual es la cantidad de estrellas minima? (1-5): '),
    read(ESTRELLAS),
    write('\tCuantas personas viajaran?: '),
    read(CANTIDAD_PERSONAS),
    write('\tCual es el tipo de habitacion? (simple, doble): '),
    read(TIPO_HABITACION),
    write('\tCuantos dias se hospedaran?: '),
    read(CANTIDAD_DIAS),
    write('\tCuantos tiempos de comida incluiras diarios? (0-3): '),
    read(TIEMPOS_COMIDA),
    write('\tViajas en vehiculo? (s/n): '),
    read(VEHICULO),
    inf_medio_presupuesto(PRESUPUESTO, CLIMA, DISTANCIA_MAX, ESTRELLAS, CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO).

alto_presupuesto(PRESUPUESTO) :- write('** Plan de viaje premium **'), nl,
    write('\tQue clima prefieres en el hotel? (calor, frio, templado, tropical): '),
    read(CLIMA),
    write('\tEl hotel debe tener clientes extranjeros? (s/n): '),
    read(CLIENTES_EXTRANJEROS),
    write('\tSe requiere un chef? (s/n): '),
    read(CHEF),
    write('\tCuantas personas viajaran?: '),
    read(CANTIDAD_PERSONAS),
    write('\tCual es el tipo de habitacion? (simple, doble): '),
    read(TIPO_HABITACION),
    write('\tCuantos dias se hospedaran?: '),
    read(CANTIDAD_DIAS),
    write('\tCuantos tiempos de comida incluiras diarios? (0-3): '),
    read(TIEMPOS_COMIDA),
    write('\tViajas en vehiculo? (s/n): '),
    read(VEHICULO),
    % Condiciones para clientes extranjeros o chefs
    (
        CLIENTES_EXTRANJEROS == 's' , CHEF == 's' -> inf_alto_presupuesto(PRESUPUESTO, CLIMA, _, 'Chef', CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO);
        CLIENTES_EXTRANJEROS == 's', CHEF == 'n' -> inf_alto_presupuesto(PRESUPUESTO, CLIMA, _, _, CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO);
        CLIENTES_EXTRANJEROS == 'n', CHEF == 's' -> inf_alto_presupuesto(PRESUPUESTO, CLIMA, 'Guatemala', 'Chef', CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO);
        CLIENTES_EXTRANJEROS == 'n', CHEF == 'n' -> inf_alto_presupuesto(PRESUPUESTO, CLIMA, 'Guatemala', _, CANTIDAD_PERSONAS, TIPO_HABITACION, CANTIDAD_DIAS, TIEMPOS_COMIDA, VEHICULO)
    ).
